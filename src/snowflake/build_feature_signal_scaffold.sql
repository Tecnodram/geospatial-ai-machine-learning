-- Family C: upstream pressure proxies for train (v1)
CREATE OR REPLACE TABLE EY_WQ.FEAT_SIG.UPSTREAM_PRESSURE_V1_TRAIN AS
SELECT
    tm.LATITUDE,
    tm.LONGITUDE,
    tm.SAMPLE_DATE,
    sc.UPSTREAM_AREA_KM2 * COALESCE(sc.CROPLAND_FRACTION_5KM, 0) AS UPSTREAM_CROPLAND_PRESSURE,
    sc.UPSTREAM_AREA_KM2 * COALESCE(sc.URBAN_FRACTION_5KM, 0) AS UPSTREAM_URBAN_PRESSURE,
    sc.UPSTREAM_AREA_KM2 * COALESCE(sc.SOIL_CLAY_0_5, 0) AS UPSTREAM_SOIL_CLAY_PRESSURE,
    sc.UPSTREAM_AREA_KM2 * COALESCE(sc.SOIL_SOC_0_5, 0) AS UPSTREAM_SOC_PRESSURE,
    sc.UPSTREAM_AREA_KM2 * COALESCE(tm.RAIN_ROLL_30, 0) AS UPSTREAM_RAIN_30D_PRESSURE,
    sc.UPSTREAM_AREA_KM2 * COALESCE(tm.RAIN_ROLL_60, 0) AS UPSTREAM_RAIN_60D_PRESSURE,
    (sc.UPSTREAM_AREA_KM2 * COALESCE(tm.RAIN_ROLL_30, 0)) / (COALESCE(sc.RIVER_DISCHARGE_CMS, 0) + 1.0) AS NUTRIENT_TRANSPORT_PROXY,
    COALESCE(tm.RAIN_ROLL_30, 0) * COALESCE(sc.SLOPE, 0) * EXP(-COALESCE(sc.DIST_TO_RIVER_M, 0) / 5000.0) AS RUNOFF_TRANSFER_INDEX,
    sc.UPSTREAM_AREA_KM2 / (COALESCE(sc.DIST_TO_RIVER_M, 0) + 1.0) AS CONNECTIVITY_PROXY,
    sc.UPSTREAM_AREA_KM2 * COALESCE(sc.SLOPE, 0) AS STREAM_POWER_PROXY
FROM EY_WQ.FEAT_SIG.TEMPORAL_MEMORY_V1_TRAIN tm
LEFT JOIN EY_WQ.FEAT_SIG.STATION_CONTEXT_V1 sc
USING (LATITUDE, LONGITUDE);

